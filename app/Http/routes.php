    Route::get('/', function () {
        return view('layouts.app');
    });
    Route::post('contact', array('as' => 'contact_post', 'uses' => 'ContactController@store'));
    Route::post('registration', array('as' => 'registration_store', 'uses' => 'RegistrationControlelr@store'));
});
